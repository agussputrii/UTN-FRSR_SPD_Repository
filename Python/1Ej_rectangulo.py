alto = int(input("Ingrese el alto del rectángulo: "))
ancho = int(input("Ingrese el ancho del rectángulo: "))

print("\nEl alto del rectángulo es: ", alto)
print("El ancho del rectángulo es: ", ancho)
print("El área del rectángulo es: ", alto * ancho)
print("El perímetro del rectángulo es: ", (alto + ancho) * 2)